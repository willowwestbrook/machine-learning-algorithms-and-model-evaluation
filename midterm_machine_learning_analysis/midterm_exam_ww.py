import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from scipy.cluster.hierarchy import linkage, dendrogram, fcluster
from sklearn.preprocessing import StandardScaler, OneHotEncoder
from sklearn.pipeline import Pipeline
from sklearn.compose import ColumnTransformer
from scipy.spatial.distance import pdist, squareform

# Load dataset
df = pd.read_csv('product_monthly_panel.csv', usecols=['rating', 'helpfulness', 'price_usd', 'skin_tone', 'eye_color', 'skin_type', 'hair_color'])

# Drop missing values
df = df.dropna()

# Define categorical and numerical features
categorical_features = ['skin_tone', 'eye_color', 'skin_type', 'hair_color']
numerical_features = ['rating', 'helpfulness', 'price_usd']

# Preprocessing: One-hot encode categorical data, scale numerical data
preprocessor = ColumnTransformer(
    transformers=[
        ('num', StandardScaler(), numerical_features),
        ('cat', OneHotEncoder(handle_unknown='ignore', sparse_output=False), categorical_features)
    ]
)

# Apply transformations
df_processed = preprocessor.fit_transform(df)

# Convert processed data to dense format
if hasattr(df_processed, "toarray"):
    df_processed = df_processed.toarray()

# Compute distance matrix to avoid memory overload
distance_matrix = pdist(df_processed, metric='euclidean')

# Perform hierarchical clustering using Ward's linkage
Z = linkage(distance_matrix, method='ward')

# Plot the dendrogram
plt.figure(figsize=(10, 5))
dendrogram(Z, truncate_mode='level', p=5)
plt.title('Hierarchical Clustering Dendrogram')
plt.xlabel('Data Points')
plt.ylabel('Distance')
plt.show()

# Determine clusters
num_clusters = 4  # Adjust as needed
cluster_labels = fcluster(Z, num_clusters, criterion='maxclust')
df['Cluster'] = cluster_labels

# Display cluster summary statistics
cluster_summary = df.groupby('Cluster')[numerical_features].mean()
print("\nCluster Summary Statistics:")
print(cluster_summary)

# Visualize clusters
plt.figure(figsize=(8,6))
sns.boxplot(x='Cluster', y='price_usd', data=df)
plt.title('Price Distribution Across Clusters')
plt.show()