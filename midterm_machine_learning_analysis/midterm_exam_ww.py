import pandas as pd
from mlxtend.frequent_patterns import apriori, association_rules
from mlxtend.preprocessing import TransactionEncoder
import matplotlib.pyplot as plt
import seaborn as sns

# Load the Product Monthly Panel dataset
file_path = 'product_monthly_panel.csv'
df = pd.read_csv(file_path, usecols=['brand_name', 'skin_type', 'skin_tone', 'eye_color', 'hair_color'])

# Drop rows with missing values in categorical columns
df = df.dropna()

# Convert categorical data into a list of transactions
transactions = df.apply(lambda x: list(x), axis=1).tolist()

# Encode transactions using TransactionEncoder
te = TransactionEncoder()
te_ary = te.fit_transform(transactions)
df_encoded = pd.DataFrame(te_ary, columns=te.columns_)

# Apply the Apriori algorithm to find frequent itemsets
frequent_itemsets = apriori(df_encoded, min_support=0.05, use_colnames=True)

# Generate association rules with a minimum confidence of 0.6
rules = association_rules(frequent_itemsets, metric='confidence', min_threshold=0.6)

print('Frequent Itemsets:')
print(frequent_itemsets)

print('Association Rules:')
print(rules)

# Generate association rules with a lower confidence threshold
rules = association_rules(frequent_itemsets, metric='confidence', min_threshold=0.4)  # Lowered from 0.6

# Adjust thresholds to avoid empty results
high_confidence_lift_rules = rules[(rules['confidence'] > 0.5) & (rules['lift'] > 1.2)]  # Lowered from 0.7 and 1.5

# Display results
print("\nAssociation Rules:")
print(high_confidence_lift_rules)

# Business Insights
if not high_confidence_lift_rules.empty:
    top_rules = high_confidence_lift_rules.sort_values(by='lift', ascending=False).head(2)
    for _, rule in top_rules.iterrows():
        print(f"\nBusiness Insight: If a customer has {rule['antecedents']}, they are likely to also have {rule['consequents']}. This could suggest targeted product bundling or personalized marketing strategies.")

# Ensure there are rules before plotting
if not high_confidence_lift_rules.empty:
    # Visualizing association rules
    plt.figure(figsize=(8,6))
    sns.scatterplot(
        x='support',
        y='confidence',
        size='lift',
        data=high_confidence_lift_rules,
        hue='lift',
        palette='viridis',
        sizes=(100, 1000),
        alpha=0.7
    )
    plt.title('Association Rules Scatter Plot')
    plt.xlabel('Support')
    plt.ylabel('Confidence')
    plt.legend(title='Lift', bbox_to_anchor=(1.05, 1), loc='upper left')
    plt.tight_layout()
    plt.show()
else:
    print("\nNo association rules met the confidence and lift thresholds, so no plot is generated.")

# Display top association rules sorted by lift
sorted_rules = high_confidence_lift_rules.sort_values(by='lift', ascending=False)
print("\nTop Association Rules Sorted by Lift:")
print(sorted_rules)

